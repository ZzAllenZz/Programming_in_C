package day01;//声明一个包叫day01

public class HelloWorld { //声明类叫HelloWorld
   //main叫做主方法，为程序的入口
   //程序的执行从main开始，main结束则程序结束
	public static void main(String[] args) {
		//输出HelloWorld
		//1）Java 是严格区分大小写的
		//2）所有符号必须英文格式
		//3）每句话必须以分号结尾
		//4)println:输出并换行， ln表示line换行
		//5）print：输出不换行
      System.out.println("HelloWorld");
      System.out.print("今天下雨啦");
	}

}
